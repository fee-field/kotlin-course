//Контекст: Создайте функцию, которая конвертирует температуру из градусов Цельсия в Фаренгейты и наоборот в зависимости
// от указанной единицы измерения (C/F). Единицу измерения нужно передать вторым аргументом функции. Несколько аргументов
// передаются через запятую. Возвращать нужно строку.
//Подсказка: для генерации строки из числа и буквы можно использовать шаблон “$result F” для фаренгейта или
// “$result C” для цельсия

fun convertTemp(degree: Int, tempUnit: String) : String {
    return if (tempUnit == "C") {
        val result = (degree*9/5) + 32
        "$result F"
    } else if (tempUnit == "F") {
        val result = (degree - 32)*5/9
        "$result C"
    }
    else {
        "incorrect value"
    }

}

//Задание 7: "Подбор Одежды по Погоде"
//Контекст: Напишите функцию, которая на основе температуры воздуха рекомендует тип одежды: "куртка и шапка"
// при температуре ниже 0, "ветровка" от 0 до 15 градусов и "футболка и шорты" при температуре выше 15.
// При температурах ниже -30 и выше +35 рекомендуйте не выходить из дома.

fun clothingType(temp: Int) : String {
    return when(temp) {
        in -30.. 0 -> "куртка и шапка"
        in 0..15 -> "ветровка"
        in 15..35 -> "футболка и шорты"
        else -> "не выходите из дома"
    }
}


//Задание 1: "Определение Сезона"
//Контекст: Напишите функцию, которая на основе номера месяца возвращает сезон года.

fun season(months: Int) : String{
    return when(months) {
        in 3..5 -> "spring"
        in 6..8 -> "summer"
        in 9..11 -> "autumn"
        in 1..2, 12 -> "winter"
        else -> "invalid input"
    }
}

